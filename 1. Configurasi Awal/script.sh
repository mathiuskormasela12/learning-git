# Untuk mengatur username & email dari git kita.
# Jika punya akun github, bitbucket atau VCS lain nya
# kita harus menuliskan email dan username 
# yg sesuai dengan akun kita itu

# Setup git username
git config --global user.name "Mathius"

# Setup git email
git config --global user.email mathiuskormasela12@gmail.com

# Menampilkan List config Git
git config --list